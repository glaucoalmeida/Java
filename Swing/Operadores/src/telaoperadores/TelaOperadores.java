    private void btnCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcActionPerformed
        pnlDiv.setVisible(true);
        int n = Integer.parseInt(txtNum.getText());
        int d = Integer.parseInt(txtDen.getText());
        float div = (float) n/d;
        double rn = Math.sqrt(n);
        double rd = Math.sqrt(d);
        lblDiv.setText(String.format("%.2f",div));
        lblRaizNum.setText(String.format("%.2f",rn));
        lblRaizDen.setText(String.format("%.2f",rd));
    }//GEN-LAST:event_btnCalcActionPerformed

